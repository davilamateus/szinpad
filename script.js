// SELECT TICKETS
function ticketsSelect(){

    // CLEANER FILTRES
    ticketsFilterAvailabre = [];
    ticketsAvailabre = [];
    ticketsFilterCharis = [];

    // SORTING OUT ALL ITENS AVAILABRE
    API.map(item=>{
        if(item.status == true){
            ticketsAvailabre.push(item);
        }
    })

    // REMOVE CLASS SELECTED ALL ITENS
    for(let i = 0; i<selected.length ; i++){
        selected[i].classList.remove('selected');
    }
    

    // ORDERING ITEMS
    for(let i = 0; i< ticketsAvailabre.length; i++){
        if(ticketsAvailabre[i].position == 'top'){
            ticketsFilterAvailabre.push(ticketsAvailabre[i]);
        }
    }
    for(let i = 0; i< ticketsAvailabre.length; i++){
        if(ticketsAvailabre[i].position == 'good'){
            ticketsFilterAvailabre.push(ticketsAvailabre[i]);
        }
    }
    for(let i = 0; i< ticketsAvailabre.length; i++){
        if(ticketsAvailabre[i].position == 'medium'){
            ticketsFilterAvailabre.push(ticketsAvailabre[i]); 
        }
    }
    for(let i = 0; i< ticketsAvailabre.length; i++){
        if(ticketsAvailabre[i].position == 'budget'){
            ticketsFilterAvailabre.push(ticketsAvailabre[i]);
        }
    }

    

    
    //VERIFYING THAT IT MEETS THE REQUIREMENTS
    for(let i = 0; i< ticketsFilterAvailabre.length-1; i++){
        if(ticketsFilterAvailabre[i+numberOfTickets] !== undefined){
            if( ticketsFilterAvailabre[i+numberOfTickets].number == ticketsFilterAvailabre[i].number+numberOfTickets 
                && ticketsFilterAvailabre[i].row == ticketsFilterAvailabre[i+numberOfTickets].row){
            ticketsFilterCharis.push(ticketsFilterAvailabre[i]);
            }
       }
    }


    //CATCHING THE FIRST THAT MEETS THE REQUIREMENT
    if(ticketsFilterCharis.find((user) => user.position == 'top')){
        item = ticketsFilterCharis.find((user) => user.position == 'top');
    } else{
        if(ticketsFilterCharis.find((user) => user.position == 'good')){
           item =  ticketsFilterCharis.find((user) => user.position == 'good');
            } else{
                if(ticketsFilterCharis.find((user) => user.position == 'medium')){
                   item =  ticketsFilterCharis.find((user) => user.position == 'medium');
                    } else{
                        if(ticketsFilterCharis.find((user) => user.position == 'budget')){
                           item =  ticketsFilterCharis.find((user) => user.position == 'budget');
                            } else{
                                error()
                            }
                        }
                }
        } 


    // ADD CLASS SELECTED
    setTimeout(() => {
        for(let i = 0; i<numberOfTickets+1;i++){
             document.getElementById((item.number+i)+"@"+(item.row)).getElementsByTagName('span')[0].classList.add('selected');
             document.getElementById((item.number+i)+"@"+(item.row)).getElementsByTagName('div')[0].classList.add('selected');
        }
    
    }, 1); 

    // STATISTIC HTML
    document.querySelector('.total-of-tickets').innerHTML= countChairs;
    document.querySelector('.total-available').innerHTML= ticketsAvailabre.length;
}



